#ifdef ENCODER_ENABLE
void encoder_update_user(uint8_t index, bool clockwise) {
	if (index == 0) {
		// Volume control
		if (clockwise) {
			tap_code(KC_RGHT);
		} else {
			tap_code(KC_LEFT);
		}
	}
	else if (index == 1) {
		// Page up/Page down
		if (clockwise) {
			tap_code(KC_DOWN);
		} else {
			tap_code(KC_UP);
		}
	}
}
#endif
